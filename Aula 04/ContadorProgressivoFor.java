public class ContadorProgressivoFor {
    public static void main(String args[]) {
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println("\nAcabou!");
		System.out.println("\n");
		
		int j;
		for (j = 0; j < 10; j++) {
            System.out.print(j + " ");
        }
        System.out.println("\nAcabou!");
    }
}

