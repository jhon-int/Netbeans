public class ContadorRegressivoFor {
    public static void main(String args[]) {
        for (int i = 9; i > -1; i--) {
            System.out.print(i + " ");
        }
        System.out.println("\nAcabou!");
		System.out.println("\n");
		
		int j;
		for (j = 9; j >= 0; j--) {
            System.out.print(j + " ");
        }
        System.out.println("\nAcabou!");
    }
}

